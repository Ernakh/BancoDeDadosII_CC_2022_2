
--EXTRA - Trigger

--Um sistema possui uma tabela para armazenar os registros dos PRODUTOS que comercializa, e tambem uma tabela de REQUISICOES que pode ser de entrada ou de saída, além da MOVIMENTACAO, que pode ser de entrada ou de saida - uma requisição que foi confirmada.

--Faça uma trigger que gere uma nova REQUISICAO de entrada caso as requisições de saída atinjam mais de 70% da quantidade do item em questão em estoque.
